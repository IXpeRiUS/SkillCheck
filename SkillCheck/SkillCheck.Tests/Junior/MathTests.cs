using Microsoft.Extensions.Configuration.UserSecrets;
using NUnit.Framework;
using SkillCheck.Models;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace SkillCheck.Tests.Junior
{
    public class MathTests
    {
        /*
         * Тест на вычисление среднего значения чисел:
         */

        [Test]
        public void calculate_average_of_numbers_and_returns_average_value()
        {
            double result = MathHelper.CalculateAverage(new int[] { 1, 2, 3, 4, 5 });
            Assert.That(result, Is.EqualTo(3.0)); // Среднее значение 1+2+3+4+5 = 3.0
        }

        /*
         * Тест на поиск минимального числа в списке:
         */

        [Test]
        public void find_minimum_number_in_list_and_returns_min_value()
        {
            int result = MathHelper.FindMinimum(new List<int> { 3, 7, 1, 9 });
            Assert.That(result, Is.EqualTo(1)); // Ожидается минимальное число = 1
        }

        /*
         * Тест на обработку исключения при делении на ноль:
         */

        [Test]
        public void divide_by_zero_and_throws_divide_by_zero_exception()
        {
            Assert.Throws<DivideByZeroException>(() => MathHelper.Divide(10, 0));
        }

        /*
         * Тест на проверку простого числа:
         */

        [Test]
        public void check_if_number_is_prime_and_returns_true()
        {
            bool result = MathHelper.IsPrime(7);
            Assert.IsTrue(result); // 7 — простое число
        }

        [Test]
        public void check_if_number_is_not_prime_and_returns_false()
        {
            bool result = MathHelper.IsPrime(10);
            Assert.IsFalse(result); // 10 — не является простым числом
        }
    }
}
